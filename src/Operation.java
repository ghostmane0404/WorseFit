public class Operation {

    private String name;
    private  int size;
    private int unique;

    public void setUnique(int unique) {
        this.unique = unique;
    }

    public int getUnique() {
        return unique;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }

}
